#include <stdio.h>

int mainaddoperation()
{
    int num1 = 10, num2 = 20, res;
    
    res = num1 + num2;
    printf("Sum of numbers %d + %d = %d", num1,num2,res);
    
    return 0;
}
int mainmultiplyoperation()
{
    int num1 = 10, num2 = 20, res;

    res = num1 * num2;
    printf("multiple of numbers %d + %d = %d", num1,num2,res);

    return 0;
}

